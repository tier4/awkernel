//! Minimal 8x8 bitmap font for overlaying short labels on boot bars.
//!
//! Each glyph is 8 rows x 8 columns, stored as 8 bytes — one byte per row,
//! MSB is the leftmost column (bit 7 = column 0).  A set bit = pixel on.
//!
//! Only the characters used by boot-stage diagnostic labels are defined;
//! unknown characters fall back to a solid filled block so missing glyphs
//! are visually obvious on-screen.

pub const GLYPH_W: usize = 8;
pub const GLYPH_H: usize = 8;

/// Solid filled 8x8 block — used as the fallback for unknown characters.
const FALLBACK: [u8; 8] = [0xFF; 8];

/// Look up the 8x8 glyph for an ASCII character.  Returns the fallback block
/// (solid square) for characters outside the supported set.
pub fn glyph(c: u8) -> &'static [u8; 8] {
    match c {
        b' ' => &SPACE,
        b'=' => &EQ,
        b':' => &COL,
        b'_' => &UND,
        b'/' => &SLA,
        b'.' => &DOT,
        b'0' => &D0, b'1' => &D1, b'2' => &D2, b'3' => &D3, b'4' => &D4,
        b'5' => &D5, b'6' => &D6, b'7' => &D7, b'8' => &D8, b'9' => &D9,
        b'A' => &LA, b'B' => &LB, b'C' => &LC, b'D' => &LD, b'E' => &LE,
        b'F' => &LF, b'G' => &LG, b'H' => &LH, b'I' => &LI, b'J' => &LJ,
        b'K' => &LK, b'L' => &LL, b'M' => &LM, b'N' => &LN, b'O' => &LO,
        b'P' => &LP, b'Q' => &LQ, b'R' => &LR, b'S' => &LS, b'T' => &LT,
        b'U' => &LU, b'V' => &LV, b'W' => &LW, b'X' => &LX, b'Y' => &LY,
        b'Z' => &LZ,
        _    => &FALLBACK,
    }
}

